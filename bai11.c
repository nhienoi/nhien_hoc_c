// Bạn hãy viết chương trình tạo ra 2 biến a và b kiểu số nguyên, gán giá trị cho a = 6, b = 2. Sau đó hiển thị ra màn hình:

// a + b = {P1}
// a - b = {P2}
// a * b = {P3}
// a / b = {P4}
// {P1} là tổng của a và b.

// {P2} là hiệu của a và b.

// {P3} là tích của a và b.

// {P4} là thương của a và b.
#include <stdio.h>
int main() {
    int a = 6;
    int b = 2;
    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);
    return 0;
}